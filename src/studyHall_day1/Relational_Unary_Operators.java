package studyHall_day1;

public class Relational_Unary_Operators {
    public static void main(String[] args) {
        // Relational operators
        int a=20;
        int b=25;

        boolean exe= a==b;
        //== equal operator
        System.out.println(a==b);
        //!= not equal operator
        System.out.println(a!=b);
        //>grater than operator
        System.out.println(a>b);
        //<less than operator
        System.out.println(a<b);
        //>= grater than or equal operator
        System.out.println(a>=b);
        //<= less than or equal operator
        System.out.println(a<=b);

    }
}
